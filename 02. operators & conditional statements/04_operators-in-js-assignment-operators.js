// operators in js
// assignment operators
// =, +=, -=, *=, /=, %=, **=
a = 5;
b = 2;
a += 4; // a = a + 4
console.log("a += 4 = ", a);
a -= 4; // a = a - 4
console.log("a -= 4 = ", a);
a *= 4; // a = a * 4
console.log("a *= 4 = ", a);
a /= 4; // a = a / 4
console.log("a /= 4 = ", a);
a %= 4; // a = a % 4
console.log("a %= 4 = ", a);
a **= 4; // a = a ** 4
console.log("a **= 4 = ", a);
