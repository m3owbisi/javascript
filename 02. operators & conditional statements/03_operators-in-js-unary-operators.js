// operators in js
// unary operators
// ++ -> increment
// -- -> decrement
a = 5;
b = 2;
c = a++; // post-increment
console.log("a++ = ", a);
c = a--; // post-decrement
console.log("a-- = ", a);
c = ++a; // pre-increment
console.log("++a = ", ++a);
c = --a; // pre-decrement
console.log("--a = ", --a);
