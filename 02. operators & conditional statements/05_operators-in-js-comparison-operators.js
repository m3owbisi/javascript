// operators in js
// comparison operators
// >, <, >=, <=
/*
equal to ==,
equal to & type ===,
not equal to !=,
not equal to & type !==
*/
a = 5;
b = 2; // b = "5"; -> "5" type === !==
console.log("5 > 2 = ", a > b);
console.log("5 < 2 = ", a < b);
console.log("5 >= 2 = ", a >= b);
console.log("5 <= 2 = ", a <= b);
console.log("5 == 2 = ", a == b);
console.log("5 === 2 = ", a === b);
console.log("5!= 2 = ", a != b);
console.log("5!== 2 = ", a !== b);
