// operators in js

// arithmetic operators
// +, -, *, /
// % -> modulus
// ** -> exponentiation
let a = 5;
let b = 2;
console.log("a = ", a, " & b =", b);
let c = a + b;
console.log("a + b = ", a + b);
c = a - b;
console.log("a - b = ", a - b);
c = a * b;
console.log("a * b = ", a * b);
c = a / b;
console.log("a / b = ", a / b);
c = a % b;
console.log("a % b = ", a % b);
c = a ** b;
console.log("a ** b = ", a ** b);
